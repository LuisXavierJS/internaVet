import UIKit

extension UIColor{
    static var darkGreen: UIColor{
        return UIColor.init(red: 0, green: 52/255, blue: 10/255, alpha: 1)
    }
    static var mediumGreen: UIColor{
        return UIColor.init(red: 0/255, green: 113/255, blue: 20/255, alpha: 1)
    }
    static var lightGreen: UIColor{
        return UIColor.init(red: 129/255, green: 178/255, blue: 117/255, alpha: 1)
    }
    static var smoothGreen: UIColor{
        return UIColor.init(red: 118/255, green: 247/255, blue: 170/255, alpha: 0.2)
    }
}
